    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            String tID = t1.getText();
            String desc = t2.getText();
            String weight = t3.getText();
            String vol = t4.getText();
            String from = t5.getText();
            String to = t6.getText();
            String ConNo = t7.getText();
            String Cont = t8.getText();
            String price = t9.getText();
            query = "Insert into tracking values("+tID+", '"+desc+"', "+weight+", '"+vol+"', '"+from+"', '"+to+"', 'Dispatched',\n" +
                    " '"+ConNo+"', "+Cont+", 'Unpaid', "+price+", 'Departure Harbour');";
            st.executeUpdate(query);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Record Updated!");
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

