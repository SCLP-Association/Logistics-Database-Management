    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
    try {
        var trackingID = t1.getText();
        query = "SELECT from_place, to_place, container_no, deli_status, live_loc FROM tracking "
                + "WHERE tracking_id = '"+trackingID+"'";
        ResultSet rs = st.executeQuery(query);
        
        while (rs.next()) {
            var from_place = rs.getString("from_place");
            String to_place = rs.getString("to_place");
            String container_no = rs.getString("container_no");
            String deli_status = rs.getString("deli_status");
            String loc = rs.getString("live_loc");
            String tbData[] = {from_place, to_place, container_no, deli_status, loc};
            DefaultTableModel tblModel = (DefaultTableModel) jTable2.getModel();
            tblModel.addRow(tbData);
            
        }
        
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, ex.toString());
    }
        
    }//GEN-LAST:event_jButton2ActionPerformed

